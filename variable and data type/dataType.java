public class dataType {
    public static void main(String[] args) {
        int a=4;
        float b=10.5f;
        double c=23.44D;
        boolean d=true;
        long e=345676553L;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
