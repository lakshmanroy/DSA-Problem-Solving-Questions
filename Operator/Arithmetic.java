import java.lang.*;

public class Arithmetic {
    public static void main(String[] args) {
        int a=5;
        int b=15;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
